    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name_ar' => 'required|min:1'
        ]);

        $section = Section::create($request->request->all());
        if ($section) {
            $request->hasFile('image') ? $this->saveMimes($section, $request, ['image'], ['1024', '1024'], true) : null;
            return redirect()->route('page.index')->with('success', 'Section Stored :)');
        }
        return redirect()->back()->with('errors', 'Section Not Stored');
    }

