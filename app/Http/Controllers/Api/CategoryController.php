     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $elements = new CategoryCollection(Category::active()->onlyParent()->with('children.children')->orderBy('order','asc')->get());
        return response()->json($elements,200);
    }

    public function search(CategoryFilters $filters)
    {
        $elements = new CategoryCollection(Category::active()->filters($filters)
            ->with('children')
            ->orderBy('id', 'desc')
            ->paginate(Self::TAKE_LESS)
            ->setPath('')
            ->withQueryString());
        return $elements;
    }

